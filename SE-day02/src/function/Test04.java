package function;

public class Test04 {
        private String name;
        private int gao;
        private int nl;
        private String age;
        private int money;

    public Test04() {
    }

    public Test04(String name, int gao, int nl, String age, int money) {
        this.name = name;
        this.gao = gao;
        this.nl = nl;
        this.age = age;
        this.money = money;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getGao() {
        return gao;
    }

    public void setGao(int gao) {
        this.gao = gao;
    }

    public int getNl() {
        return nl;
    }

    public void setNl(int nl) {
        this.nl = nl;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }
}

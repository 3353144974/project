package operation.As7._06;

public class Employee {
   String name;
   double xz;

    public Employee() {
    }

    public Employee(String name, double xz) {
        this.name = name;
        this.xz = xz;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getXz() {
        return xz;
    }

    public void setXz(double xz) {
        this.xz = xz;
    }
}
